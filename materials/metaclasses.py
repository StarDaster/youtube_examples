# Метаклассы - классы, которые сами являются классами. Идея в том, чтобы собирать не просто экземпляры
# классов, а по сути сами классы, которые являются экземплярами класса type.

# применение:
# - логирование и профилирование
# - тестирвоание интерфейса
# - регистрация классов во время создания
# - автоматическое создание свойств
# - прокси
# - автоматическая блокировка / синхронизация ресурсов
